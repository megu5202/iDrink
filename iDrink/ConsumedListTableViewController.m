- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"Cell";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if(cell == nil)
    {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleValue1 reuseIdentifier:CellIdentifier];
    }
    
    // Configure the cell...
    
    cell.textLabel.text = [self.drinksDrank objectAtIndex:indexPath.row];
    cell.detailTextLabel.text = [NSDateFormatter localizedStringFromDate:[self.timesDrank objectAtIndex:indexPath.row] dateStyle:NSDateFormatterNoStyle timeStyle:NSDateFormatterShortStyle];
    
    return cell;
}


